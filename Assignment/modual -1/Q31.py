# Q - 31  Write a Python program that manipulates and prints strings using various string methods. 

str = " This is python!"

print(str)

#convert into upper case
print(str.upper())

#convert into lover case
print(str.lower())

#replace a word
print(str.replace("python","programming"))

#find a position a position of word
print(str.find("python"))

#split the string 
print(str.split())

#join the string
print("-".join(str))

#capitalize the first letter
print(str.capitalize())

#Capitalizes the first letter of each word
print(str.title())

# Returns the length of the string
print(len(str))

# Removes leading and trailing spaces
print(str.strip())


